# membuat list nilai
nilai = [70, 85, 60, 90, 75]

# menghitung rata rata nilai
rata_rata = sum(nilai) / len(nilai)

# menemukan nilai tertinggi
nilai_tertinggi = max(nilai)

# mencari berapa kali angka 80 muncul dalam list
jumlah_80 = nilai.count(80)

# penampilan hasil
print("list nilai :", nilai)
print("rata rata nilai :", rata_rata)
print("nilai tertinggi :", nilai_tertinggi)
print("jumlah kemunculan angka 80 :", jumlah_80 )